let exchangeRate: ExchangeRate = {};
let globalValue: string;
let globalCurrencyTo: string;

chrome.runtime.onMessage.addListener((message, sender, sendResponse) => {
  const { units, currencyTo } = message;
  globalValue = units;
  globalCurrencyTo = currencyTo;

  
  fetchCurrencyRates();
  console.log("Received message from popup:", currencyTo);
});

const fetchCurrencyRates = async () => {
  try {
    const response = await fetch(
      
      `https://api.exchangerate.host/latest?base=${globalCurrencyTo}&timestamp=${Date.now()}`
    );

    const data = await response.json();
    
    console.log(data)
    chrome.storage.local.set(
      { key: data, extensionName: "Converto" },
      function () {
        console.log("Data saved to local storage:", data);
      }
    );
  } catch (error) {
    console.error("Error fetching currency rates:", error);
  }
};

chrome.tabs.onCreated.addListener((tab) => {
  if (!tab.url) {
    setInterval(fetchCurrencyRates, 6 * 60 * 60 * 1000); 
  }
});




